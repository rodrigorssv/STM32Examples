/* USER CODE BEGIN 4 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	if(huart == &huart1) {
		if(protocolBegin == false) {
			if(uart_char_rx == UART_START) {
				uart_index = 0;
				protocolBegin = true;
			}
		}
		else {
			uart_raw_rx[uart_index] = uart_char_rx;
			uart_index++;
			ITM_SendChar(uart_char_rx);
			if(uart_char_rx == UART_TERMINATOR) {
				if(uart_raw_rx[0] == 'G' && uart_raw_rx[1] == 'P' &&
					uart_raw_rx[2] == 'R' && uart_raw_rx[3] == 'M' &&
					uart_raw_rx[4] == 'C') {
						memcpy(GPS_Rx.uart_buff_rx, uart_raw_rx, uart_index-1);
						GPS_Rx.size = uart_index;
				}

				uart_index = 0;
				protocolBegin = false;
			}
		}
		HAL_UART_Receive_IT(&huart1, &uart_char_rx, 1);
	}
}
